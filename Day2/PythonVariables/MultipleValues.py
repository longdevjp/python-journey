# Many Values to Multiple Variables
name, age, country = "Long", 20, "VN"
print(name)
print(age)      
print(country)

# One Value to Multiple Variables
x = y = z = "Long"
print(x)
print(y)
print(z)

# Unpack a Collection
People = ["Long", "20", "VN"]
name, age, country = People
print(name)
print(age)
print(country)
